#Operadores Lógicos

a = True
b = False

print(a, b)

a and b  #Os dois precisariam ser verdadeiro para retornar True, como o 'b' é falso, ele retornará False.
a & b    #Outra opçao do mesmo código

c = a and b
print("'A' e 'B' são iguais é", c)

#a or b  - Retornará True, caso uma dessa variáveis seja verdadeiro, ele retornará True.

a | b

d = a or b
print ("'a' ou 'b' é igual a", d)

#not a  - Operador Not é igual a Negação, ele retorna o valor ao contrário.

